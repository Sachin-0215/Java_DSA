
public class bitwise {

    public static void main(String args[]) {
        // bitwise AND
        System.out.println(5 & 6);

        // bitwise OR
        System.out.println(5 | 6);
        
        // bitwise XOR
        System.out.println(5 ^ 6);

        //bitwise 1's complement
        System.out.println(~0 + " " + ~1);

        //right shift operator
        System.out.println(6 >> 2);

        //left shift operator
        System.out.println(4<<3);
        
    }
}